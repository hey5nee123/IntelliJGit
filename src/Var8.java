public class Var8 {
    public static void main(String[] args) {
        //정수(4) 높을 수록 메모리 공간 많이 차지.
        byte b =127; // -128~127;
        short s = 32767; //-32,768 ~ 32,767
        int i = 2147483647; //-2,147,483,648~ 2,147,483,647

        long l = 929390292303299L; //여튼 많이. 대신 뒤에 L 붙여야 함!


        //실수(2)
        float f = 10.0f; //뒤에 f 붙여주세요.
        double d = 10.0; //실수 중 기본형. 정밀도 up


    }
}
